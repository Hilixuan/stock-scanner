def get_trading_date() -> str:
    """返回最近一个交易日（跳过周末）。"""
    d = datetime.now()
    while d.weekday() >= 5:  # Sat=5, Sun=6
        d -= timedelta(days=1)
    return d.strftime("%Y-%m-%d")

